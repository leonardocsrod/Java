package com.loiane.cursojava.aula11;

public class VariaveisFlutuantes {

	public static void main(String[] args) {
		double valorPassagem = 2.99;
		float valorPassageNovo = 3.99f;
		
		System.out.println("Valor da passagem: " + valorPassagem);
		System.out.println("Valor da passagem nova: " + valorPassageNovo);
	}

}
