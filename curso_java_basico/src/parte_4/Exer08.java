package parte_4;

public class Exer08 {

	public static void main(String[] args) {
		//8. Operadores de Incremento
		//Declare uma variável contador com valor inicial de 5.
		//Use o operador de incremento ++ para 	aumentar o valor e 
		//exiba o novo valor. Em seguida, use o operador de decremento -- 
		//para diminuir o valor e mostre o resultado.

		int numero = 5;
		System.out.println("Número: "+ numero);
		numero++;
		System.out.println("Número após incremento ++: "+ numero);
		numero--;
		System.out.println("Número após decremento --: "+ numero);

	}

}
